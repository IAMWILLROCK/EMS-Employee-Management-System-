package com.luv2code.springdemo.dto;


public class ResDetailsDto{
	
	private String resourceId;
	
	private String firstName;
	
	private String lastName;
	
	private String mobile;
	
	private String email;
		
	private String designationId;
	
	private String subGradeId;
	
	private String departmentId;
	
	private String pSkills;
	
	private String sSkills;
	
	public ResDetailsDto() {
		
	}

	public String getResourceId() {
		return resourceId;
	}

	public void setResourceId(String resourceId) {
		this.resourceId = resourceId;
	}

	public String getFirstName() {
		return firstName;
	}

	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}

	public String getLastName() {
		return lastName;
	}

	public void setLastName(String lastName) {
		this.lastName = lastName;
	}

	public String getMobile() {
		return mobile;
	}

	public void setMobile(String mobile) {
		this.mobile = mobile;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getDesignationId() {
		return designationId;
	}

	public void setDesignationId(String designationId) {
		this.designationId = designationId;
	}

	public String getDepartmentId() {
		return departmentId;
	}

	public void setDepartmentId(String departmentId) {
		this.departmentId = departmentId;
	}

	public String getpSkills() {
		return pSkills;
	}

	public void setpSkills(String pSkills) {
		this.pSkills = pSkills;
	}

	public String getsSkills() {
		return sSkills;
	}

	public void setsSkills(String sSkills) {
		this.sSkills = sSkills;
	}

	public String getSubGradeId() {
		return subGradeId;
	}

	public void setSubGradeId(String subGradeId) {
		this.subGradeId = subGradeId;
	}


	
	
}

